namespace OptiCore.Enums;

/// <summary>
/// Defines whether the optimization problem seeks to maximize or minimize the objective function.
/// </summary>
public enum ObjectiveType
{
    /// <summary>
    /// Maximize the objective function value (find the largest possible value).
    /// </summary>
    MAX,

    /// <summary>
    /// Minimize the objective function value (find the smallest possible value).
    /// </summary>
    MIN
}
